package Encapsulation.Example3Employee;

public class Employee {
    private int id;
    private String name;

    public void setid(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    // ----------------------------------
    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
}
